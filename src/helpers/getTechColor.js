export const getTechColor = tech => {
  if (tech === 'ANDROID') {
    return '#FCDB35';
  }
  if (tech === 'IOS') {
    return '#B8FB3B';
  }
  if (tech === 'NODE_JS') {
    return '#C252FE';
  }
  if (tech === 'RAILS') {
    return '#FE8352';
  }
  if (tech === 'REACT') {
    return '#FE529D';
  }
  return '#fff';
};
